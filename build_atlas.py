"""Build suyn keyboard-overlay atlases for input-overlay (OBS plugin).

Idle row: cream rounded card with the key's letter in Chalkboard SE Bold (mint green).
Pressed row: cream rounded card with a chibi sticker.
Row 3 has a wide spacebar tile with an avatar on the left and a logo on the right.

Outputs two PNG atlases (series1, series2) at output/wasd-suyn.png + wasd-suyn2.png.
The matching JSON layouts live next to them at output/wasd-suyn.json + wasd-suyn2.json
and are NOT regenerated by this script (they are hand-authored, identical for both series).
"""
from PIL import Image, ImageDraw, ImageFilter, ImageFont
import os

HERE = os.path.dirname(os.path.abspath(__file__))
ASSETS = os.path.join(HERE, "assets")
OUTPUT = os.path.join(HERE, "output")

TILE = 192
CARD_INSET = 22
PAD = 28
RADIUS = 22
CARD_RGBA = (255, 246, 229, 235)
SHADOW_RGBA = (180, 140, 170, 90)
SHADOW_BLUR = 6

# Per-column (key_label, asset_filename_in_series_dir).
# The 12 columns map to atlas columns 0..11.
